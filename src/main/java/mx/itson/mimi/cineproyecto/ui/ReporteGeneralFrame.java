    public ReporteGeneralFrame(List<Boleto> listaBoletos, CineProyecto cineProyecto) {
        this.listaBoletos = listaBoletos;
        this.cineProyecto = cineProyecto;


        setTitle("Reporte General de Ventas");
        setSize(500, 400);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setLayout(new BorderLayout());


        txtReporteGeneral = new JTextArea();
        txtReporteGeneral.setEditable(false);
        cargarReporte();

        JScrollPane scrollPane = new JScrollPane(txtReporteGeneral);


        lblTotales = new JLabel();
        actualizarTotales();


        JButton btnVolver = new JButton("Volver");
        btnVolver.addActionListener(e -> {
            new MainFrame(cineProyecto).setVisible(true);  
            dispose();  
        });

        JPanel panelInferior = new JPanel(new BorderLayout());
        panelInferior.add(lblTotales, BorderLayout.CENTER);
        panelInferior.add(btnVolver, BorderLayout.EAST);

        add(scrollPane, BorderLayout.CENTER);
        add(panelInferior, BorderLayout.SOUTH);

        setLocationRelativeTo(null);  // Tengo hambre
    }

    private void cargarReporte() {
        if (listaBoletos.isEmpty()) {
            txtReporteGeneral.setText("No hay boletos vendidos");
            return;
        }

        StringBuilder reporte = new StringBuilder();
        for (Boleto boleto : listaBoletos) {
            reporte.append(String.format("Cliente: %s | Pel√≠cula: %s | Asiento: %s | Precio: $%.2f\n",
                boleto.getCliente(), boleto.getPelicula(), boleto.getAsiento(), boleto.getPrecio()));
        }
        txtReporteGeneral.setText(reporte.toString());
    }

    private void actualizarTotales() {
        int totalBoletos = listaBoletos.size();
        double gananciaTotal = listaBoletos.stream().mapToDouble(Boleto::getPrecio).sum();
        lblTotales.setText(String.format("Total Boletos Vendidos: %d | Ganancia Total: $%.2f", totalBoletos, gananciaTotal));
    }



